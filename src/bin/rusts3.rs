use std::net::SocketAddr;
use std::sync::Arc;

use clap::Parser;
use rust_s3_server::server::{
    config::AppConfig,
    serve, S3HttpConfig,
    logging::init_logging,
};

/// S3-compatible local dev server.
///
/// All configuration lives in a YAML file.
/// Run `rusts3 --init` to generate a documented `config.yaml` in the current
/// directory, then `rusts3 -c config.yaml` to start the server.
#[derive(Parser, Debug)]
#[command(name = "rusts3", version)]
struct Args {
    /// Path to config.yaml.
    #[arg(short = 'c', long, value_name = "FILE")]
    config: Option<String>,

    /// Write a default config.yaml to the current directory and exit.
    /// Exits with an error if the file already exists.
    #[arg(long)]
    init: bool,
}

/// Documented YAML template written by `--init`.
const DEFAULT_CONFIG_YAML: &str = r#"# rusts3 configuration
# Generated by: rusts3 --init
# Start the server with: rusts3 -c config.yaml

# ─── Server ───────────────────────────────────────────────────────────────────
server:
  # IP address to listen on.  0.0.0.0 = all interfaces.
  bind_address: "0.0.0.0"

  # TCP port.
  bind_port: 8002

  # Root directory where bucket data is stored on disk.
  # Each bucket becomes a sub-directory: <base_dir>/buckets/<bucket-name>/
  base_dir: "./rusts3-data"

# ─── Storage ──────────────────────────────────────────────────────────────────
storage:
  # Maximum SQLite connections kept open per bucket index pool.
  sqlite_max_connections: 50

  # Maximum object metadata entries in the LRU cache.
  # Each entry is roughly 400–700 bytes; 200 000 entries ≈ 80–140 MB.
  # HEAD and GET requests are served from this cache on warm hits.
  meta_cache_capacity: 200000

  # Maximum entries in the SQLite-repair suppression cache (key → last-checked timestamp).
  # Each entry is roughly 100 bytes; 200 000 entries ≈ 20 MB.
  sqlite_repair_cache_capacity: 200000

# ─── Logging ──────────────────────────────────────────────────────────────────
logging:
  # Minimum log level: trace | debug | info | warn | error
  level: "info"

  # Emit aggregate bandwidth totals and rates every 10 seconds.
  enable_bandwidth_report: true

  # Path to a rolling log file.  Omit to log to stdout only.
  # file: "/var/log/rusts3/rusts3.log"

  # Rotate when the log file reaches this size (MiB).
  rotation_size_mb: 100

  # Number of archived log files to keep alongside the active file.
  keep_files: 5

  # Compress archived files with gzip (.gz extension added automatically).
  compress: false

# ─── Sweeper ──────────────────────────────────────────────────────────────────
sweeper:
  # How often the background sweeper runs (seconds).
  interval_secs: 300

  # Yield after this many SQLite index entries are checked.
  # The sweeper still completes the full bucket scan before rescheduling.
  max_objects_per_pass: 100

  # Minimum age of a stale SQLite row (with missing meta.json) before removal (seconds).
  orphan_grace_period_secs: 300

  # Minimum idle age of an abandoned staging directory before removal (seconds).
  staging_expiry_secs: 86400

# ─── Authentication ───────────────────────────────────────────────────────────
auth:
  # Set to true to require AWS SigV4 signatures on every request.
  # When false the server accepts all requests without credentials (open access).
  enabled: false

  # The hostname (and optional port) that S3 clients are configured to use.
  # Example: "mys3.company.com" or "localhost:8002"
  #
  # When set, presigned URL verification uses this value for the `host` signed
  # header instead of the incoming HTTP Host header.  This makes presigned URLs
  # work correctly behind a reverse proxy that rewrites the Host header.
  # Clients must configure their endpoint to match this value exactly.
  #
  # When omitted (default), the incoming Host header is used directly.
  # public_hostname: "localhost:8002"

  # List of valid access-key / secret-key pairs.
  # credentials:
  #   - access_key: "minioadmin"
  #     secret_key: "minioadmin"
"#;

#[tokio::main]
async fn main() -> Result<(), Box<dyn std::error::Error>> {
    let args = Args::parse();

    if args.init {
        let path = "config.yaml";
        if std::path::Path::new(path).exists() {
            eprintln!("error: {path} already exists — remove it first if you want to regenerate.");
            std::process::exit(1);
        }
        std::fs::write(path, DEFAULT_CONFIG_YAML)?;
        println!("Wrote {path}. Edit it, then run: rusts3 -c {path}");
        return Ok(());
    }

    let cfg = match &args.config {
        Some(path) => AppConfig::from_file(path)?,
        None => AppConfig::default(),
    };

    init_logging(&cfg.logging)?;

    let address: SocketAddr =
        format!("{}:{}", cfg.server.bind_address, cfg.server.bind_port).parse()?;

    log::info!(
        "rusts3 starting on {address} base_dir={} auth={}",
        cfg.server.base_dir,
        if cfg.auth.enabled {
            "enabled"
        } else {
            "disabled"
        },
    );

    serve(S3HttpConfig {
        address,
        root: cfg.server.base_dir.clone(),
        app_config: Arc::new(cfg),
    })
    .await
}
