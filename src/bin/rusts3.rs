#[tokio::main]
async fn main() -> Result<(), Box<dyn std::error::Error>> {
    let cli = Cli::parse();
    match cli.command {
        Some(Command::Run { config }) => run_server(AppConfig::from_file(&config)?).await,
        Some(Command::GenPassword { cost, password }) => {
            let password = match password {
                Some(value) => value,
                None => {
                    let first = rpassword::prompt_password("Password: ")?;
                    let second = rpassword::prompt_password("Confirm password: ")?;
                    if first != second {
                        return Err("passwords do not match".into());
                    }
                    first
                }
            };
            println!("{}", bcrypt::hash(password, cost)?);
            Ok(())
        }
        Some(Command::VerifyPassword { hash, cleartext }) => {
            let hash = match hash {
                Some(value) => value,
                None => prompt_line("Bcrypt hash: ")?,
            };
            let cleartext = match cleartext {
                Some(value) => value,
                None => rpassword::prompt_password("Cleartext password: ")?,
            };
            if bcrypt::verify(cleartext, hash.trim())? {
                println!("password matches");
                Ok(())
            } else {
                eprintln!("password does not match");
                std::process::exit(1);
            }
        }
        Some(Command::Validate { config }) => {
            AppConfig::from_file(&config)?;
            println!("configuration is valid: {config}");
            Ok(())
        }
        Some(Command::Init) => write_default_config(),
        None if cli.init => write_default_config(),
        None => {
            let cfg = match cli.config {
                Some(path) => AppConfig::from_file(&path)?,
                None => AppConfig::default(),
            };
            run_server(cfg).await
        }
    }
}

fn write_default_config() -> Result<(), Box<dyn std::error::Error>> {
    let path = "config.yaml";
    if std::path::Path::new(path).exists() {
        return Err(format!(
            "{path} already exists — remove it first if you want to regenerate it"
        )
        .into());
    }
    std::fs::write(path, DEFAULT_CONFIG_YAML)?;
    println!("Wrote {path}. Edit it, then run: rusts3 run -c {path}");
    Ok(())
}

fn prompt_line(prompt: &str) -> Result<String, Box<dyn std::error::Error>> {
    use std::io::Write;
    print!("{prompt}");
    std::io::stdout().flush()?;
    let mut value = String::new();
    std::io::stdin().read_line(&mut value)?;
    Ok(value.trim_end().to_string())
}

async fn run_server(cfg: AppConfig) -> Result<(), Box<dyn std::error::Error>> {

    init_logging(&cfg.logging)?;

    let address: SocketAddr =
        format!("{}:{}", cfg.server.bind_address, cfg.server.bind_port).parse()?;

    log::info!(
        "rusts3 starting on {address} base_dir={} auth={}",
        cfg.server.base_dir,
        if cfg.auth.enabled {
            "enabled"
        } else {
            "disabled"
        },
    );

    serve(S3HttpConfig {
        address,
        root: cfg.server.base_dir.clone(),
        app_config: Arc::new(cfg),
    })
    .await
}

#[cfg(test)]
mod tests {
    use super::{Cli, Command};
    use clap::Parser;

    #[test]
    fn run_and_validate_default_to_config_yaml() {
        let run = Cli::try_parse_from(["rusts3", "run"]).unwrap();
        assert!(matches!(
            run.command,
            Some(Command::Run { config }) if config == "config.yaml"
        ));

        let validate = Cli::try_parse_from(["rusts3", "validate"]).unwrap();
        assert!(matches!(
            validate.command,
            Some(Command::Validate { config }) if config == "config.yaml"
        ));
    }

    #[test]
    fn password_subcommand_names_are_unhyphenated() {
        assert!(Cli::try_parse_from([
            "rusts3",
            "genpassword",
            "--password",
            "example-password"
        ])
        .is_ok());
        assert!(Cli::try_parse_from([
            "rusts3",
            "verifypassword",
            "$2b$12$abcdefghijklmnopqrstuuuuuuuuuuuuuuuuuuuuuuuuuuuuu",
            "--cleartext",
            "example-password"
        ])
        .is_ok());
    }
}
